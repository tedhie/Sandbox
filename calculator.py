while 1 == 1:
    x = int(input("Input 1: "))
    z = input("+,-,*,/")
    y = int(input("Input 2: "))
    if z == "+":
        b = x + y
        print(b)
    elif z == "-":
        b = x - y
        print(b)
    elif z == "*":
        b = x * y
        print(b)
    elif z == "/":
        b = x / y
        print(b)
